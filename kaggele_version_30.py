# Tab-separated metadata: id \t date \t url \t title \t description(caption)
META_TEMPLATE = "%(id)s\t%(upload_date>%Y-%m-%d)s\t%(webpage_url)s\t%(title)s\t%(description)s"


def download_channel(ch: dict) -> tuple:
    """Download one TikTok channel. Runs in a thread."""
    name = ch["name"]
    url  = ch["url"]
    out_dir  = os.path.join(BASE_NETRAL, name, "videos")
    meta_csv = os.path.join(BASE_NETRAL, name, "metadata.csv")
    os.makedirs(out_dir, exist_ok=True)

    cmd = [
        "yt-dlp",
        "-o",  f"{out_dir}/%(upload_date>%Y-%m-%d)s_%(id)s.%(ext)s",
        "-f",  "best[ext=mp4]",
        "-I",  f"1:{MAX_VIDEOS}",
        "--concurrent-fragments", str(CONCURRENT_FRAGMENTS),
        "--print-to-file", META_TEMPLATE, meta_csv,
        url,
    ]

    result = subprocess.run(cmd, capture_output=True, text=True)
    print(f"[{name}] exit={result.returncode}")
    if result.stderr:
        for line in result.stderr.splitlines()[-5:]:   # last 5 error lines
            print(f"  [{name}] {line}")
    return name, result.returncode


print(f"Downloading {len(CHANNELS)} channels in parallel...")
with ThreadPoolExecutor(max_workers=len(CHANNELS)) as pool:
    futures = {pool.submit(download_channel, ch): ch["name"] for ch in CHANNELS}
    results = {}
    for future in as_completed(futures):
        name, rc = future.result()
        results[name] = rc

print("Download results:", results)


