def build_file_map(videos_path):
    """Map video_id → filename for a /videos directory."""
    file_map = {}
    if not os.path.exists(videos_path):
        return file_map
    for f in os.listdir(videos_path):
        if not f.lower().endswith(VIDEO_EXT):
            continue
        name = os.path.splitext(f)[0]
        file_map[name] = f
        if "_" in name:
            file_map[name.split("_")[-1]] = f
    return file_map


def load_metadata(csv_path):
    """Load metadata CSV robustly; handles comma and tab separators."""
    for sep in ("\t", ",", None):
        try:
            kwargs = {"header": None, "engine": "python", "on_bad_lines": "skip"}
            if sep:
                kwargs["sep"] = sep
            df = pd.read_csv(csv_path, **kwargs)
            if len(df.columns) >= 4:
                break
        except Exception:
            continue

    ncols = len(df.columns)
    if ncols >= 5:
        df = df.iloc[:, :5]
        df.columns = ["video_id", "date", "url", "title", "caption_post"]
    else:
        df = df.iloc[:, :4]
        df.columns = ["video_id", "date", "url", "title"]
        df["caption_post"] = ""

    df["video_id"] = df["video_id"].astype(str).str.strip().str.replace('"', '')
    df["title"] = df["title"].astype(str).str.strip()
    df["caption_post"] = df["caption_post"].astype(str).str.strip()
    return df


def build_video_index(folder_path):
    """Map video_id fragments → full path for a channel folder."""
    index = {}
    videos_path = os.path.join(folder_path, "videos")
    if not os.path.exists(videos_path):
        return index
    for f in os.listdir(videos_path):
        if f.lower().endswith(VIDEO_EXT):
            full = os.path.join(videos_path, f)
            name = f.split(".")[0]
            index[name] = full
            for part in name.split("_"):
                index[part] = full
    return index


